class REODQADataset(Dataset):
    """
    Representation Engineering for Open-Domain Question-Answering
    """

    def __init__(self, tokenizer, data, shots_set, model_name,align_base=True,
                 demonstration_pool_size=128, task="initial_inference_without_intervention"):
        super(REODQADataset, self).__init__()
        self.k_shot_candidates = None
        self.tokenizer = tokenizer
        self.data = data
        self.idx2item = {item["idx"]: item for item in self.data}
        self.shots_set = shots_set
        self.group2ids = dict()
        self.idx2group = dict()
        self.group_distinct_questions()
        self.shots_groups = self.get_shots_groups()
        candidate_demonstration_groups = list(range(len(self.shots_groups)))
        np.random.RandomState(42).shuffle(candidate_demonstration_groups)  # fixed seed
        self.demonstration_groups_ids = candidate_demonstration_groups[:demonstration_pool_size]
        self.num_distinct_questions = len(list(self.group2ids.keys()))
        self.align_base = align_base
        self.model_name = model_name
        self.task = task

        # self.seed = seed
        self.select_k_demonstrations_group_ids = None
        if self.task in ["test_prompts"]:
            # only iterate over hold-out demonstrations
            self.data_for_iter = [item for item in self.data if self.idx2group[item["idx"]]
                                  not in self.demonstration_groups_ids]
            self.sample_keys = set()   

    def get_shots_groups(self):
        shots_groups = set()
        for idx in self.shots_set:
            group_idx = self.idx2group[idx]
            shots_groups.add(group_idx)
        return list(shots_groups)

    def group_distinct_questions(self):
        distinct_question_group = defaultdict(list)
        for item in self.data:
            distinct_question_group[item["question"]].append(item["idx"])
        self.group2ids = {group_idx: ids for group_idx, ids in enumerate(distinct_question_group.values())}
        for group_idx, idx_list in self.group2ids.items():
            for idx in idx_list:
                self.idx2group[idx] = group_idx

    

    def collect_hiddens_dataloader(self, k_shot, seed, batch_size=1, num_workers=20, shuffle=False):
        """
        This dataloader is used to
        1. check the knowledge selection under documnts,
        2. collect the hidden states that will lead to different knowledge selection behaviours
        3. record the logprob of generating org_answer and sub_answer
        """

        rng = np.random.RandomState(seed)

        def collate_fn(batch):
            item = batch[0]
            item_idx = item["idx"]

            demonstration_ids = self.sample_demonstrations(item_idx, k_shot, rng)
            sample_key = str(item_idx) + "-" + "-".join([str(idx) for idx in demonstration_ids])
            while sample_key in self.sample_keys:
                demonstration_ids = self.sample_demonstrations(item_idx, k_shot, rng)
                sample_key = str(item_idx) + "-" + "-".join([str(idx) for idx in demonstration_ids])
            if self.align_base:
                prompt_no = self.get_prompt(item, self.model_name, None, 1, ['qa','no'],self.tokenizer)
                prompt_c = self.get_prompt(item, self.model_name, item["documents"], 1,['qa','c'],self.tokenizer)
                prompt_a = self.get_prompt(item, self.model_name, None, 1,['qa','a'],self.tokenizer)
                prompt_ca = self.get_prompt(item, self.model_name, item["documents"], 1,['qa','ca'],self.tokenizer)
                prompts = {"no": prompt_no,
                        "c": prompt_c,
                        "a": prompt_a,
                        "ca": prompt_ca}
                test_prompts_input_ids = {f"{k}": self.tokenizer([v], return_tensors="pt")["input_ids"]
                                    for k, v in prompts.items()}

                prompt_no = self.get_prompt(item, self.model_name, None, 1, ['qa_a','no'],self.tokenizer)
                prompt_c = self.get_prompt(item, self.model_name, item["documents"], 1,['qa_a','c'],self.tokenizer)
                prompt_a = self.get_prompt(item, self.model_name, None, 1,['qa_a','a'],self.tokenizer)
                prompt_ca = self.get_prompt(item, self.model_name, item["documents"], 1,['qa_a','ca'],self.tokenizer)
                prompts = {"no": prompt_no,
                        "c": prompt_c,
                        "a": prompt_a,
                        "ca": prompt_ca}
                prompts_input_ids = {f"{k}_a": self.tokenizer([v], return_tensors="pt")["input_ids"]
                                    for k, v in prompts.items()}

                return_dict = {"answer": [item["answer"]],
                            "documents": [item["documents"]],
                            "item_idx": item_idx,
                            "demonstration_ids": demonstration_ids}
                return_dict.update(test_prompts_input_ids)
                return_dict.update(prompts_input_ids)
                return return_dict
            else:
                demonstrations = [self.idx2item[idx] for idx in demonstration_ids]

                no_prompt = self.verbalise_demonstrations(demonstrations, None, "answer")
                c_prompt = self.verbalise_demonstrations(demonstrations, "documents", "answer")
                a_prompt = self.verbalise_demonstrations(demonstrations, "answer", "answer")
                ca_prompt = self.verbalise_demonstrations(demonstrations, "documents_answer", "answer")

                test_example_prompt_no = self.verbalise_one_example(item, None, None, is_test=True)
                test_example_prompt_c = self.verbalise_one_example(item, "documents", None, is_test=True)
                test_example_prompt_a = self.verbalise_one_example(item, "answer", None, is_test=True)
                test_example_prompt_ca = self.verbalise_one_example(item, "documents_answer", None, is_test=True)
                test_prompts = {"no": no_prompt + test_example_prompt_no,
                        "c": c_prompt + test_example_prompt_c,
                        "a": a_prompt + test_example_prompt_a,
                        "ca": ca_prompt + test_example_prompt_ca}
                test_prompts_input_ids = {f"{k}": self.tokenizer([v], return_tensors="pt")["input_ids"]
                                    for k, v in test_prompts.items()}

                prompt_no = self.verbalise_one_example(item, None, "answer")
                prompt_c = self.verbalise_one_example(item, "documents", "answer")
                prompt_a = self.verbalise_one_example(item, "answer", "answer")
                prompt_ca = self.verbalise_one_example(item, "documents_answer", "answer")
                prompts = {"no": no_prompt + prompt_no,
                        "c": c_prompt + prompt_c,
                        "a": a_prompt + prompt_a,
                        "ca": ca_prompt + prompt_ca}
                prompts_input_ids = {f"{k}_a": self.tokenizer([v], return_tensors="pt")["input_ids"]
                                    for k, v in prompts.items()}

                return_dict = {"answer": [item["answer"]],
                            "documents": [item["documents"]],
                            "item_idx": item_idx,
                            "demonstration_ids": demonstration_ids}
                return_dict.update(test_prompts_input_ids)
                return_dict.update(prompts_input_ids)
                return return_dict

        return DataLoader(self, batch_size=batch_size, shuffle=shuffle, num_workers=num_workers, collate_fn=collate_fn)
    
    def sample_demonstrations(self, test_item_idx, num_demonstrations, rng):
        test_item_group_idx = self.idx2group[test_item_idx]
        candidate_group_ids = [gi for gi in self.demonstration_groups_ids if gi != test_item_group_idx]
        selected_group_ids = rng.choice(candidate_group_ids, size=num_demonstrations, replace=False)
        selected_ids = []
        for group_idx in selected_group_ids:
            selected_ids.append(rng.choice(self.group2ids[group_idx], 1).item())
        return selected_ids

    def verbalise_one_example(self, example, ctx_key, ans_key, is_test=False):
        if ctx_key=="documents":
            prompt = "documents: " + example[ctx_key][0] + "\n"
        elif ctx_key=="answer":
            prompt = "reference answer: " + example["answer"][0] + "\n"
        elif ctx_key=="documents_answer":
            prompt = "documents: " + example["documents"][0] + "\n" + "reference answer: " + example["answer"][0] + "\n"
        else:
            prompt = ""
        prompt = prompt + "question: " + example["question"] + "\n"
        if is_test:
            prompt = prompt + "answer:"
        else:
            prompt = prompt + "answer: " + example[ans_key][0] + "\n\n"
        return prompt

    def get_prompt(self, sample, model_name, doc_lis, doc_num, prompt_type,tokenizer):
        paras = ""
        ref_key = 'question'
        prompt = prompt_dict[prompt_type[0]]['none'] # prior
