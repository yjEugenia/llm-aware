class MMLUMCDataset(Dataset):
    """
    Zero-shot MMLU Multi-Choice Dataset
    - No reference documents
    - No few-shot demonstrations
    - Source file: parquet
    """

    def __init__(self, 
                 parquet_path,
                 tokenizer,
                 model_name,
                 task_type="mc_qa",  # mc_qa / mc_qa_cot / mc_qa_prior / mc_qa_post
                 align_base=True,
                 sample_ratio=1.0,
                 sample_seed=42):

        super(MMLUMCDataset, self).__init__()

        self.data = pd.read_parquet(parquet_path)
        if sample_ratio < 1.0:
            total_len = len(self.data)
            sample_size = max(1, int(total_len * sample_ratio))

            self.data = self.data.sample(
                n=sample_size,
                random_state=sample_seed
            ).reset_index(drop=True)
        self.tokenizer = tokenizer
        self.model_name = model_name
        self.task_type = task_type
        self.align_base = align_base
        self.all_choices = ['A', 'B', 'C', 'D']

        self.template_prompt = model_template_dict[model_name]

    # ------------------------------------------------
    # Prompt Construction
    # ------------------------------------------------

    def build_question_text(self, sample):
        """
        Construct multi-choice question text
        """

        question = sample["question"]

        choices = [
            f"A. {sample['choices'][0]}",
            f"B. {sample['choices'][1]}",
            f"C. {sample['choices'][2]}",
            f"D. {sample['choices'][3]}",
        ]

        question_text = question + "\n" + "\n".join(choices)

        return question_text

    def get_prompt(self, sample):

        question_text = self.build_question_text(sample)

        prompt_template = prompt_dict[self.task_type]['none']
        tail = prompt_dict[self.task_type]['tail']

        subject = ""
        if "subject" in sample:
            subject = f" about {sample['subject']}"

        prompt = prompt_template.format(
            subject=subject,
            question=question_text,
            paras="",
            prediction=""
        ) + tail

        full_prompt = (
            self.template_prompt['prefix']
            + prompt
            + self.template_prompt['end']
        )

        return full_prompt

    # ------------------------------------------------
    # Dataloader
    # ------------------------------------------------

    def get_dataloader(self,
                       batch_size=1,
                       num_workers=4,
                       shuffle=False):

        def collate_fn(batch):

            sample = batch[0]

            prompt = self.get_prompt(sample)

            input_ids = self.tokenizer(
                [prompt],
                return_tensors="pt"
            )["input_ids"]
            return {
                **sample,
                "input": prompt,
                "input_ids": input_ids,
                "label": self.all_choices[sample["answer"]]
            }

        return DataLoader(
            self,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            collate_fn=collate_fn
        )

    # ------------------------------------------------
    # PyTorch Required
    # ------------------------------------------------

    def __getitem__(self, idx):
        return self.data.iloc[idx].to_dict()

    def __len__(self):
        return len(self.data)

        
