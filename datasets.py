        """
       
        """
        labels = torch.full_like(input_ids, -100)
        
        start_char_idx = prompt_text.rfind(question_text)
        if start_char_idx == -1:
            print(f"Warning: Question not found in prompt!")
            return labels
            
        end_char_idx = start_char_idx + len(question_text)
        
        prefix_str = prompt_text[:start_char_idx]
        prefix_with_q_str = prompt_text[:end_char_idx]
        
        start_tok_idx = len(self.tokenizer.encode(prefix_str, add_special_tokens=True))
        end_tok_idx = len(self.tokenizer.encode(prefix_with_q_str, add_special_tokens=True))
        
        for i in range(len(input_ids)):
            s_idx = min(start_tok_idx, input_ids.shape[1])
            e_idx = min(end_tok_idx, input_ids.shape[1])
            labels[i, s_idx:e_idx] = input_ids[i, s_idx:e_idx]
            
        return labels
    
